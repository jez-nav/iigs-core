public enum IIGSCharacterGeneratorSource: Equatable, Sendable {
    case fallback
    case characterROM
}

public struct IIGSCharacterGlyph: Equatable, Sendable {
    public static let width = 7
    public static let height = 8

    public var rows: [UInt8]

    public init(rows: [UInt8]) {
        precondition(rows.count == Self.height)
        self.rows = rows.map { $0 & 0x7F }
    }

    public func pixelLit(x: Int, y: Int) -> Bool {
        precondition((0..<Self.width).contains(x))
        precondition((0..<Self.height).contains(y))
        return rows[y] & (0x40 >> UInt8(x)) != 0
    }

    func inverted() -> IIGSCharacterGlyph {
        IIGSCharacterGlyph(rows: rows.map { ~$0 & 0x7F })
    }
}

public struct IIGSCharacterGenerator: Equatable, Sendable {
    public private(set) var source: IIGSCharacterGeneratorSource

    private var glyphRows: [UInt8]

    public init(source: IIGSCharacterGeneratorSource = .fallback, glyphRows: [UInt8]? = nil) {
        self.source = source
        if let glyphRows {
            precondition(glyphRows.count == 256 * IIGSCharacterGlyph.height)
            self.glyphRows = glyphRows.map { $0 & 0x7F }
        } else {
            self.glyphRows = Self.makeFallbackRows()
        }
    }

    public init(characterROM bytes: [UInt8], bank: Int = 0, invertedBits: Bool = false) {
        let bankSize = 256 * IIGSCharacterGlyph.height
        precondition(bank >= 0)
        precondition(bytes.count >= bankSize)
        let bankOffset = min(bank * bankSize, max(0, bytes.count - bankSize))
        let sourceRows = bytes[bankOffset..<(bankOffset + bankSize)]
        self.source = .characterROM
        self.glyphRows = sourceRows.map { byte in
            let row = invertedBits ? ~byte : byte
            return row & 0x7F
        }
    }

    public mutating func loadCharacterROM(_ bytes: [UInt8], bank: Int = 0, invertedBits: Bool = false) {
        self = IIGSCharacterGenerator(characterROM: bytes, bank: bank, invertedBits: invertedBits)
    }

    public func glyph(forScreenByte byte: UInt8, alternateCharacterSet: Bool, flashPhase: Bool = false) -> IIGSCharacterGlyph {
        if source == .fallback,
           alternateCharacterSet,
           (0x40...0x5F).contains(byte),
           let rows = Self.fallbackMouseTextRows(for: byte) {
            return IIGSCharacterGlyph(rows: rows)
        }

        let resolved = resolvedGlyphCode(forScreenByte: byte, alternateCharacterSet: alternateCharacterSet, flashPhase: flashPhase)
        var glyph = glyph(forCode: resolved.code)
        if resolved.inverted {
            glyph = glyph.inverted()
        }
        return glyph
    }

    public func glyph(forCode code: UInt8) -> IIGSCharacterGlyph {
        let offset = Int(code) * IIGSCharacterGlyph.height
        return IIGSCharacterGlyph(rows: Array(glyphRows[offset..<(offset + IIGSCharacterGlyph.height)]))
    }

    private func resolvedGlyphCode(
        forScreenByte byte: UInt8,
        alternateCharacterSet: Bool,
        flashPhase: Bool
    ) -> (code: UInt8, inverted: Bool) {
        switch byte {
        case 0x00...0x3F:
            return (byte | 0x40, true)
        case 0x40...0x5F where alternateCharacterSet:
            return (byte, false)
        case 0x60...0x7F where alternateCharacterSet:
            return (byte, false)
        case 0x40...0x7F:
            return (byte, flashPhase)
        default:
            return (byte & 0x7F, false)
        }
    }

    private static func makeFallbackRows() -> [UInt8] {
        var rows = Array(repeating: UInt8(0), count: 256 * IIGSCharacterGlyph.height)

        func set(_ code: UInt8, _ glyph: [UInt8]) {
            let offset = Int(code) * IIGSCharacterGlyph.height
            let padded = glyph + Array(repeating: 0, count: max(0, IIGSCharacterGlyph.height - glyph.count))
            for row in 0..<IIGSCharacterGlyph.height {
                rows[offset + row] = padded[row] & 0x7F
            }
        }

        for code in UInt8(0x20)...UInt8(0x7E) {
            set(code, asciiFallbackRows(for: code))
        }

        return rows
    }

    private static func fallbackMouseTextRows(for code: UInt8) -> [UInt8]? {
        let sourceRows: [UInt8]
        switch code {
        case 0x40:
            sourceRows = [0x08, 0x10, 0x6C, 0xFE, 0xFC, 0xFC, 0x7E, 0x6C]
        case 0x41:
            sourceRows = [0x08, 0x10, 0x6C, 0x82, 0x84, 0x84, 0x52, 0x6C]
        case 0x4C:
            sourceRows = [0xFE, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]
        case 0x53:
            sourceRows = [0x00, 0x00, 0x00, 0xFE, 0x00, 0x00, 0x00, 0x00]
        default:
            return nil
        }

        // Apple MouseText rows are stored as 7 visible bits in bits 7...1.
        return sourceRows.map { ($0 >> 1) & 0x7F }
    }

